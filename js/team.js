let teamPages = {};

function buildTeamPages(lang) {
    const t = teamI18n[lang] || teamI18n.zh;
    teamPages = {
        intro: t.intro_content,
        membersCS2: t.membersCS2_content,
        membersMC: t.membersMC_content,
        news: t.news_content,
        history: t.history_content
    };
}

function showTeam(page, btn) {
    const lang = window.__currentLang ? window.__currentLang() : 'zh';
    buildTeamPages(lang);
    document.getElementById("teamContent").innerHTML = teamPages[page] || '<p>Page not found</p>';
    document.querySelectorAll(".tool-btn").forEach(b => b.classList.remove("active"));
    if (btn) btn.classList.add("active");
}

document.addEventListener("DOMContentLoaded", () => {
    document.addEventListener('langChanged', (e) => {
        const currentPage = document.querySelector(".tool-btn.active")?.dataset.team || 'intro';
        showTeam(currentPage, document.querySelector(`.tool-btn[data-team="${currentPage}"]`));
    });

    const btns = document.querySelectorAll(".tool-btn[data-team]");
    btns.forEach(btn => {
        btn.addEventListener("click", () => {
            const page = btn.getAttribute("data-team");
            showTeam(page, btn);
        });
    });
    const defaultBtn = document.querySelector(".tool-btn[data-team='intro']");
    if (defaultBtn) showTeam("intro", defaultBtn);
});